n=(input('Insira qualquer coisa:'))
print('Tipo primitivo de ',n ,'é:',type(n))
print('E um numero?', n.isnumeric())
print('E alfanumérico?', n.isalnum())
print('E alfabético?', n.isalpha())
print('Tem espaço?', n.isspace())
print('Esta em maiusculas?', n.isupper())
print('Esta em minusculas?', n.islower())
print('Esta capitalizada?', n.istitle())












